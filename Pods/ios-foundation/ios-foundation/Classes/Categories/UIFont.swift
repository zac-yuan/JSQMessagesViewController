
import UIKit

public extension UIFont {
    
    public func babylonRegularFont(fontSize: CGFloat) -> UIFont {
        return UIFont(name: "AvenirNext-Regular", size: fontSize)!
    }
    
    public func babylonMediumFont(fontSize: CGFloat) -> UIFont {
        return UIFont(name: "AvenirNext-Medium", size: fontSize)!
    }
    
}